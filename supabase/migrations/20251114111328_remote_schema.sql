CREATE OR REPLACE FUNCTION "public"."delete_orphaned_auth_users"() RETURNS "text"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
DECLARE
  orphaned_user_id uuid;
  deleted_count int := 0;
BEGIN
  -- Vòng lặp này tìm tất cả 'id' trong 'auth.users'
  -- mà KHÔNG CÓ 'id' tương ứng trong 'public.profiles'
  FOR orphaned_user_id IN
    SELECT u.id
    FROM auth.users u
    LEFT JOIN public.profiles p ON u.id = p.id
    WHERE p.id IS NULL -- Điều kiện quan trọng: profile không tồn tại
  LOOP
    -- Gọi hàm admin để xóa người dùng một cách an toàn
    -- Hàm này sẽ xử lý mọi thứ liên quan đến auth
    PERFORM auth.delete_user(orphaned_user_id);
    deleted_count := deleted_count + 1;
  END LOOP;

  RETURN 'Đã xóa ' || deleted_count || ' người dùng mồ côi.';
END;
$$;


ALTER FUNCTION "public"."delete_orphaned_auth_users"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."get_my_profile"() RETURNS TABLE("id" "uuid", "img" "text", "name" "text", "role" "text", "email" character varying, "phone" "text", "createdat" timestamp with time zone)
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$BEGIN
    RETURN QUERY
    SELECT
        p.id,
        p.avatar_url AS img,
        p.name,
        r.role_name AS role,
        u.email,
        p.phone,
        u.created_at AS createdAt
    FROM
        public.profiles p
    LEFT JOIN
        auth.users u ON p.id = u.id
    LEFT JOIN
        public.roles r ON p.role_id = r.id
    WHERE
        p.id = auth.uid()
    LIMIT 1;
END;$$;


